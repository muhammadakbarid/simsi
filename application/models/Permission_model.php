    function get_limit_data($limit, $start = 0, $q = NULL)
    {
        //join permission dengan groups
        $this->db->select('b.*, k.name,(SELECT name from groups where id = b.id_groups_to) name2');
        $this->db->from('permission b');
        $this->db->join('groups k', 'k.id=b.id_groups');

        $this->db->order_by('b.id', $this->order);
        $this->db->like('b.id', $q);
        $this->db->or_like('k.name', $q);
        // $this->db->or_like('k.harga', $q);
        // $this->db->or_like('b.kode_menu', $q);
        $this->db->limit($limit, $start);
        return $this->db->get()->result();
    }
