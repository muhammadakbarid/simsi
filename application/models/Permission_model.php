    function get_all_by_id_groups($id_group)
    {
        /*
        SELECT u.id as id_user, u.first_name, p.id_groups as dari, ug.group_id as kepada
        FROM users u
        LEFT JOIN users_groups ug on ug.user_id = u.id
        JOIN permission p on p.id_groups_to = ug.group_id
        WHERE p.id_groups = 17
        GROUP BY ug.group_id
        */
        $this->db->select('u.id as id_user, u.first_name, p.id_groups as dari, ug.group_id as kepada');
        $this->db->from('users u');
        $this->db->join('users_groups ug', 'ug.user_id = u.id', 'left');
        $this->db->join('permission p', 'p.id_groups_to = ug.group_id');
        $this->db->where('p.id_groups', $id_group);
        $this->db->group_by('ug.group_id');

        return $this->db->get()->result();
    }

